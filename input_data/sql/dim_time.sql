SELECT 
    c.number + 1 AS id,
    ('2018-01-10' + INTERVAL c.number DAY) AS day,
    WEEK(('2018-01-10' + INTERVAL c.number DAY),
        7) AS weeknum,
    YEARWEEK(('2018-01-10' + INTERVAL c.number DAY), 7) AS yearweek,
    CASE
        WHEN
            WEEK(('2018-01-10' + INTERVAL c.number DAY),
                7) > 30
        THEN
            YEAR('2018-01-10' + INTERVAL c.number DAY) + 1
        ELSE YEAR('2018-01-10' + INTERVAL c.number DAY)
    END AS season
FROM
    (SELECT 
        singles + tens + hundreds + thousands number
    FROM
        (SELECT 0 singles UNION ALL SELECT 1 UNION ALL SELECT 2 UNION ALL SELECT 3 UNION ALL SELECT 4 UNION ALL SELECT 5 UNION ALL SELECT 6 UNION ALL SELECT 7 UNION ALL SELECT 8 UNION ALL SELECT 9) singles
    JOIN (SELECT 0 tens UNION ALL SELECT 10 UNION ALL SELECT 20 UNION ALL SELECT 30 UNION ALL SELECT 40 UNION ALL SELECT 50 UNION ALL SELECT 60 UNION ALL SELECT 70 UNION ALL SELECT 80 UNION ALL SELECT 90) tens
    JOIN (SELECT 0 hundreds UNION ALL SELECT 100 UNION ALL SELECT 200 UNION ALL SELECT 300 UNION ALL SELECT 400 UNION ALL SELECT 500 UNION ALL SELECT 600 UNION ALL SELECT 700 UNION ALL SELECT 800 UNION ALL SELECT 900) hundreds
    JOIN (SELECT 0 thousands UNION ALL SELECT 1000 UNION ALL SELECT 2000 UNION ALL SELECT 3000 UNION ALL SELECT 4000 UNION ALL SELECT 5000 UNION ALL SELECT 6000 UNION ALL SELECT 7000 UNION ALL SELECT 8000 UNION ALL SELECT 9000) thousands
    ORDER BY number ASC) c
WHERE
    c.number BETWEEN 0 AND 4000